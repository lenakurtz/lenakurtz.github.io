function show_WEC() {
      var x = document.getElementById("PopUp_WEC");
      if (x.style.display === "none") {
        x.style.display = "block";
      } else {
        x.style.display = "none";
      }
    }

function close_WEC() {
      var x = document.getElementById("PopUp_WEC");
          if (x.style.display === "none") {
            x.style.display = "block";
          } else {
            x.style.display = "none";
          }
    }
